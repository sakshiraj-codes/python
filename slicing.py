A = 'Hello_World'
print(A[4:7])  # will print 4 to 6th index
print(A[3:])  # index 3 to end
print(A[:8])  # start to index 7th
print(A[4:1:-1])  # will print 4,3 & 2nd index in this order.
print(A[9:11])  # out of range will not be printed.
print(A[::])  # whole will be printed.
print(A[::-1])  # reverse will be printed
